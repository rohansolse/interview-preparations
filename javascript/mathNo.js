console.log(10 + 2 * 3);
// Explanation:
// According to the order of operations (PEMDAS/BODMAS), multiplication is performed before addition.
// Therefore, 2 * 3 is calculated first, resulting in 6, and then 10 + 6 equals 16.

console.log('1' - - '1');
// Explanation:
// In this expression, the unary minus operator converts the string '1' to the number 1.
// Thus, the expression becomes 1 - -1, which is equivalent to 1 + 1, resulting in 2.

console.log(1 - - 1);
// Explanation:
// Here, the unary minus operator is applied to the number 1, converting it to -1.
// Therefore, the expression becomes 1 - (-1), which is equivalent to 1 + 1, resulting in 2.